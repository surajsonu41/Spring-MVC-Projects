package com.vtalent.ioc;

public class HR {
private double packageSalary;
private int leaveCount;
/**
 * @return the packageSalary
 */
public double getPackageSalary() {
	return packageSalary;
}
/**
 * @param packageSalary the packageSalary to set
 */
public void setPackageSalary(double packageSalary) {
	this.packageSalary = packageSalary;
}
/**
 * @return the leaveCount
 */
public int getLeaveCount() {
	return leaveCount;
}
/**
 * @param leaveCount the leaveCount to set
 */
public void setLeaveCount(int leaveCount) {
	this.leaveCount = leaveCount;
}


}
